package model;

public class Skola {
	private String adresa;
	private String naziv;
	private int sifra;

	public String getAdresa() {
		return this.adresa;
	}

	public void setAdresa(String adresa) {
		this.adresa = adresa;
	}

	public String getNaziv() {
		return this.naziv;
	}

	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}

	public int getSifra() {
		return this.sifra;
	}

	public void setSifra(int sifra) {
		this.sifra = sifra;
	}
}
